        <div class="sobre_nos_cont grid_8">
            <p>O QuiControle é um sistema de controle financeiro pessoal online
                descomplicado. Foi criado em 2014 por Littbarski Santos, Dejota
                Freitas e Jardel Rodrigues, três estudantes do curso de Sistema
                de Informação da Universitário Católica de Quixadá, apaixonados
                por soluções simples para problemas do dia a dia.</p>
            <br/>
            <p>A ideia nasceu depois que os três começaram a perceber que, por 
                diversas vezes, acabava o dinheiro mas não acabava o mês. Viram 
                também que isto é um problema comum de muita gente. No fim, 
                nunca era possível dizer ao certo com o que foi gasto o dinheiro
                de cada um.</p>
        </div>
        <div class="sobre_nos_cont grid_8">
            <p>Inicialmente desenvolvido apenas para smartphones Android, o 
                QuiControle cresceu para a Web. Utilizando conceitos de rede 
                social, design simples e bonito, todos os dados estão totalmente
                sincronizados e seguros utilizando a tecnologia da nuvem.</p>
            <br/>
            <p>O QuiControle vem com uma forma diferente de controlar seus gastos. 
                Com a missão de ajudar os usuários em educação financeira, 
                todos podem compartilhar dicas entre si, ver postagens no nosso 
                blog e analisar seus gastos no sistema.</p>
